% Взаимная корреляция с циклическим сдвигом
a = [0.3 0.2 -0.1 4.2 -2 1.5 0];
b = [0.3 4 -2.2 1.6 0.1 0.1 0.2];
corr_values = zeros(1, length(a));  % Массив для хранения значений корреляции

for shift = 0:length(a)-1
    b_shifted = circshift(b, shift);  % Циклический сдвиг b
    corr_values(shift + 1) = sum(a .* b_shifted);  % Вычисление взаимной корреляции
end

% Построение графика взаимной корреляции от сдвига
figure;
plot(0:length(a)-1, corr_values);
title('Зависимость взаимной корреляции от циклического сдвига');
xlabel('Циклический сдвиг');
ylabel('Взаимная корреляция');
