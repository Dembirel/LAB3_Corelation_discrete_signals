% Шаг 1: Определение времени
time = [0:100-1]/100;  % 100 точек, шаг 0.01

% Шаг 2: Определение частот
f1 = 6;      % Частота для s1(t)
f2 = 6 + 4;  % Частота для s2(t)
f3 = 6 * 2 + 1; % Частота для s3(t)

% Шаг 3: Генерация сигналов
s1 = cos(2 * pi * f1 * time);  % s1(t)
s2 = cos(2 * pi * f2 * time);  % s2(t)
s3 = cos(2 * pi * f3 * time);  % s3(t)

% Шаг 4: Формирование сигналов a(t) и b(t)
a = 4 * s1 + 4 * s2 + s3;  % a(t)
b = s1 + 1;  % b(t)

% Шаг 5: Вычисление корреляции между s1(t) и a(t)
corr_s1_a = corr(s1', a');
disp(['Корреляция между s1(t) и a(t): ', num2str(corr_s1_a)]);

% Шаг 6: Вычисление корреляции между s1(t) и b(t)
corr_s1_b = corr(s1', b');
disp(['Корреляция между s1(t) и b(t): ', num2str(corr_s1_b)]);

% Шаг 7: Нормализованная корреляция (та же, что и обычная, так как MATLAB нормирует корреляцию по умолчанию)
% Нормализованная корреляция между s1(t) и a(t)
norm_corr_s1_a = corr_s1_a;  
disp(['Нормализованная корреляция между s1(t) и a(t): ', num2str(norm_corr_s1_a)]);

% Нормализованная корреляция между s1(t) и b(t)
norm_corr_s1_b = corr_s1_b;
disp(['Нормализованная корреляция между s1(t) и b(t): ', num2str(norm_corr_s1_b)]);

% Шаг 8: Графики сигналов
figure;

% График a(t)
subplot(3, 1, 1);
plot(time, a);
title('Сигнал a(t)');
xlabel('Время (t)');
ylabel('Амплитуда');

% График b(t)
subplot(3, 1, 2);
plot(time, b);
title('Сигнал b(t)');
xlabel('Время (t)');
ylabel('Амплитуда');

% График s1(t)
subplot(3, 1, 3);
plot(time, s1);
title('Сигнал s1(t)');
xlabel('Время (t)');
ylabel('Амплитуда');
